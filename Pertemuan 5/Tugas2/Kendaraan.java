public class Kendaraan {
	protected int jmlRoda;
	protected String warna;

	public void setJmlRoda(int jmlRoda) {
		this.jmlRoda = jmlRoda;
	}

	public int getJmlRoda() {
		return this.jmlRoda;
	}

	public void setWarna(String warna) {
		this.warna = warna;
	}

	public String getWarna() {
		return this.warna;
	}
}